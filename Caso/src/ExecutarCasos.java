
public class ExecutarCasos {

	public static void main(String[] args) {
		
		int dia;
		
		dia = 1;
		
		switch (dia) {
		
		case 1:
			System.out.println("Olá meu amigo hoje é domingo");
			break;
			
		case 2:
			System.out.println("Jesus hoje é segunda-feira");
			break;
			
		case 3:
			System.out.println("Hoje é terça-feira");
			break;
			
		}

	}

}
