
public class LacoWhile {

	public static void main(String[] args) {
		
		System.out.println("Loco While");
		
		for(int contador=0; contador<=5; contador++) {
			
		System.out.println(contador);
		
		}

		System.out.println("Loco While");
		
		int contador_w=0;
		while(contador_w <=2) {
			
			System.out.println(contador_w);
			contador_w++;
		}
	}

}
